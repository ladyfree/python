# 선택한 값을 누적하기 위한 딕셔너리
scores = {"D": 0, "I": 0, "S": 0, "C": 0}

for i, question in enumerate(QUESTIONS): #1번 반복문
    random.shuffle(question)
    #print(question)
    txt = f"({i+1}질문) "
    for idx, a in enumerate(question):
        txt = f"{txt} {idx+1}:{a[0]} " # 결과 더하기

    ans = ""
    while(ans.isnumeric() == False or int(ans)<1 or int(ans)>4):
        ans = input(txt)
    
    disc = question[int(ans)-1][1]

    # 결과 누적하기
    scores[disc] += 1

sorted_scores = sorted(scores.items(), key=lambda x: x[1], reverse=True)
# print(sorted_scores)
primary, secondary = sorted_scores[0], sorted_scores[1]
print(primary, secondary)

